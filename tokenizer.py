def download_and_prepare_europarl(
    lang: str,
    output_dir: str,
    max_train: int = 10000,
    max_valid: int = 1000,
):
    lang_code = {"spanish": "es", "polish": "pl"}[lang]
    config = f"en-{lang_code}"

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    ds = load_dataset("Helsinki-NLP/europarl", config)
    rows = ds["train"]

    src_lines, tgt_lines = [], []

    for ex in rows:
        trans = ex["translation"]

        src = normalize_whitespace(trans[lang_code])
        tgt = normalize_whitespace(trans["en"])

        if src and tgt:
            src_lines.append(src)
            tgt_lines.append(tgt)

        if len(src_lines) >= max_train + max_valid:
            break

    files = {
        "train_src": str(out / f"train.{lang_code}"),
        "train_tgt": str(out / "train.en"),
        "valid_src": str(out / f"valid.{lang_code}"),
        "valid_tgt": str(out / "valid.en"),
    }

    write_lines(files["train_src"], src_lines[:max_train])
    write_lines(files["train_tgt"], tgt_lines[:max_train])
    write_lines(files["valid_src"], src_lines[max_train:max_train + max_valid])
    write_lines(files["valid_tgt"], tgt_lines[max_train:max_train + max_valid])

    return files



def split_suffix_once(word: str, lang: str) -> str:
    suffixes = SPANISH_SUFFIXES if lang == "spanish" else POLISH_SUFFIXES
    lower = word.lower()

    for suffix in suffixes:
        if len(lower) > len(suffix) + 2 and lower.endswith(suffix):
            stem = word[:-len(suffix)]
            ending = word[-len(suffix):]
            return f"{stem} @@ {ending}"

    return word


def rare_long_morph_line(
    line: str,
    lang: str,
    word_freq: Counter,
    max_freq: int,
    min_len: int,
) -> str:
    pieces = []

    for tok in simple_word_tokenize(line):
        if is_word(tok):
            word = tok.lower()

            if word_freq[word] < max_freq and len(word) >= min_len:
                pieces.append(split_suffix_once(tok, lang))
            else:
                pieces.append(tok)
        else:
            pieces.append(tok)

    return normalize_whitespace(" ".join(pieces))



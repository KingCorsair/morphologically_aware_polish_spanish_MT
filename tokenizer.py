def get_row(results, system_name):
    for row in results["comparison"]:
        if row["system"] == system_name:
            return row
    raise ValueError(f"System not found: {system_name}")


def changed_examples(
    results,
    baseline_name="baseline_bpe",
    method_name="rare_long_freq2_len8",
    max_examples=25,
):
    baseline_examples = results["all_results"][baseline_name]["examples"]
    method_examples = results["all_results"][method_name]["examples"]

    baseline_map = {ex["word"]: ex["tokens"] for ex in baseline_examples}
    method_map = {ex["word"]: ex["tokens"] for ex in method_examples}

    changed = []

    for word in method_map:
        if word not in baseline_map:
            continue

        baseline_tokens = baseline_map[word]
        method_tokens = method_map[word]

        if baseline_tokens != method_tokens:
            changed.append({
                "word": word,
                "baseline_tokens": baseline_tokens,
                "method_tokens": method_tokens,
                "baseline_token_count": len(baseline_tokens),
                "method_token_count": len(method_tokens),
                "token_count_change": len(method_tokens) - len(baseline_tokens),
            })

    changed = sorted(changed, key=lambda x: x["token_count_change"])

    return changed[:max_examples]

import torch
import evaluate
from transformers import AutoTokenizer, AutoModelForSeq2SeqLM

bleu_metric = evaluate.load("sacrebleu")
chrf_metric = evaluate.load("chrf")


def evaluate_mt_quality(src_path, tgt_path, model_name, max_examples=300):
    src_lines = read_lines(src_path)[:max_examples]
    tgt_lines = read_lines(tgt_path)[:max_examples]

    tok = AutoTokenizer.from_pretrained(model_name)
    model = AutoModelForSeq2SeqLM.from_pretrained(model_name)

    device = "cuda" if torch.cuda.is_available() else "cpu"
    model.to(device)
    model.eval()

    preds = []

    for line in src_lines:
        batch = tok(
            line,
            return_tensors="pt",
            padding=True,
            truncation=True,
            max_length=128,
        ).to(device)

        with torch.no_grad():
            output = model.generate(**batch, max_length=128)

        pred = tok.decode(output[0], skip_special_tokens=True)
        preds.append(pred)

    bleu = bleu_metric.compute(
        predictions=preds,
        references=[[x] for x in tgt_lines],
    )

    chrf = chrf_metric.compute(
        predictions=preds,
        references=tgt_lines,
    )

    return {
        "model_name": model_name,
        "num_examples": len(src_lines),
        "bleu": bleu["score"],
        "chrf": chrf["score"],
        "examples": [
            {
                "src": src_lines[i],
                "pred": preds[i],
                "ref": tgt_lines[i],
            }
            for i in range(min(5, len(src_lines)))
        ],
    }


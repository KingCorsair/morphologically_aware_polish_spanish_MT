@dataclass
class FragmentationMetrics:
    mean_tokens_per_word: float
    median_tokens_per_word: float
    p90_tokens_per_word: float
    mean_tokens_per_sentence: float
    examples_checked: int


def compute_fragmentation_metrics(
    tokenizer: Tokenizer,
    lines: Sequence[str],
    max_sentences: int = 200,
) -> FragmentationMetrics:
    selected = list(lines[:max_sentences])

    sent_token_counts = []
    word_token_counts = []

    for line in selected:
        sent_token_counts.append(len(tokenizer.encode(line).tokens))

        for word in simple_word_tokenize(line):
            if is_word(word):
                word_token_counts.append(len(tokenizer.encode(word).tokens))

    sorted_counts = sorted(word_token_counts)
    p90_idx = min(len(sorted_counts) - 1, math.floor(0.9 * len(sorted_counts)))

    return FragmentationMetrics(
        mean_tokens_per_word=statistics.mean(word_token_counts),
        median_tokens_per_word=statistics.median(word_token_counts),
        p90_tokens_per_word=float(sorted_counts[p90_idx]),
        mean_tokens_per_sentence=statistics.mean(sent_token_counts),
        examples_checked=len(selected),
    )


def sample_words(lines: Sequence[str], min_len: int = 5, max_items: int = 1000):
    counter = Counter()

    for line in lines:
        for tok in simple_word_tokenize(line):
            if is_word(tok) and len(tok) >= min_len:
                counter[tok.lower()] += 1

    return [w for w, _ in counter.most_common(max_items)]


def inspect_word_splits(tokenizer: Tokenizer, words: Sequence[str]):
    return [
        {"word": word, "tokens": tokenizer.encode(word).tokens}
        for word in words
    ]



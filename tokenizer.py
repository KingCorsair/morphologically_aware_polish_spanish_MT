def train_and_eval_system(
    name: str,
    train_lines: Sequence[str],
    valid_lines: Sequence[str],
    out_dir: Path,
    vocab_size: int,
):
    train_path = out_dir / f"{name}_train.txt"
    write_lines(train_path, train_lines)

    tokenizer = train_bpe_tokenizer(
        train_file=train_path,
        vocab_size=vocab_size,
        save_dir=out_dir / name,
    )

    metrics = compute_fragmentation_metrics(tokenizer, valid_lines)

    words = sample_words(valid_lines, min_len=5, max_items=1000)
    examples = inspect_word_splits(tokenizer, words)

    return {
        "metrics": asdict(metrics),
        "examples": examples,
    }


def run_rare_long_grid_search(
    train_src_path: str,
    valid_src_path: str,
    lang: str,
    output_dir: str,
    vocab_size: int = 8000,
    max_freq_values=(2, 3, 5, 8),
    min_len_values=(8, 10, 12, 15),
):
    out_dir = Path(output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    train_src = [
        normalize_whitespace(x)
        for x in read_lines(train_src_path)
        if normalize_whitespace(x)
    ]

    valid_src = [
        normalize_whitespace(x)
        for x in read_lines(valid_src_path)
        if normalize_whitespace(x)
    ]

    word_freq = build_word_freq(train_src)

    systems = {}

    systems["baseline_bpe"] = train_src

    for max_freq in max_freq_values:
        for min_len in min_len_values:
            system_name = f"rare_long_freq{max_freq}_len{min_len}"

            systems[system_name] = [
                rare_long_morph_line(
                    line=line,
                    lang=lang,
                    word_freq=word_freq,
                    max_freq=max_freq,
                    min_len=min_len,
                )
                for line in train_src
            ]

    results = {}

    for name, train_lines in systems.items():
        print(f"Training system: {name}")

        results[name] = train_and_eval_system(
            name=name,
            train_lines=train_lines,
            valid_lines=valid_src,
            out_dir=out_dir,
            vocab_size=vocab_size,
        )

    baseline_word = results["baseline_bpe"]["metrics"]["mean_tokens_per_word"]
    baseline_sent = results["baseline_bpe"]["metrics"]["mean_tokens_per_sentence"]

    comparison = []

    for name, data in results.items():
        metrics = data["metrics"]

        comparison.append({
            "system": name,
            "mean_tokens_per_word": metrics["mean_tokens_per_word"],
            "mean_tokens_per_sentence": metrics["mean_tokens_per_sentence"],
            "delta_word_vs_baseline": metrics["mean_tokens_per_word"] - baseline_word,
            "delta_sent_vs_baseline": metrics["mean_tokens_per_sentence"] - baseline_sent,
        })

    comparison = sorted(comparison, key=lambda x: x["mean_tokens_per_word"])

    final_output = {
        "language": lang,
        "vocab_size": vocab_size,
        "comparison": comparison,
        "all_results": results,
    }

    with open(out_dir / "rare_long_grid_results.json", "w", encoding="utf-8") as f:
        json.dump(final_output, f, ensure_ascii=False, indent=2)

    return final_output


